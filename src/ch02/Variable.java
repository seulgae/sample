package ch02;

public class Variable {

	public static void main(String[] args) {
		int fifteen = 15;
		long lightSpeed = 300000L;
		double pi  = 3.14;
		float floatPi = 3.14f;
		
		
		System.out.println(fifteen);
		System.out.println(lightSpeed);
		System.out.println(pi);
		System.out.println(floatPi);

	}

}
